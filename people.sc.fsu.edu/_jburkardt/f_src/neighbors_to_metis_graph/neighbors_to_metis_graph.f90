subroutine metis_graph_write ( output_file_name, m, n, neighbor )

!*****************************************************************************80
!
!! METIS_GRAPH_WRITE writes the METIS graph file.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    15 October 2008
!
!  Author:
!
!    Jeff Borggaard, John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) OUTPUT_FILE_NAME, the output file name.
!
!    Input, integer ( kind = 4 ) M, the number of graph edges
!
!    Input, integer ( kind = 4 ) N, the number of graph vertices.
!
!    Input, integer ( kind = 4 ) NEIGHBOR(M,N), the neighbor_file data.
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  integer ( kind = 4 ) ios
  character ( len = * ) output_file_name
  integer ( kind = 4 ) output_unit
  integer ( kind = 4 ) neighbor(m,n)

  character ( len = 30 ) string
  integer ( kind = 4 ) i
  integer ( kind = 4 ) i_positive
  integer ( kind = 4 ) j
  integer ( kind = 4 ) n_elements
  integer ( kind = 4 ) n_faces
  integer ( kind = 4 ), allocatable, dimension ( : ) :: neighbors_of_element

  call get_unit ( output_unit )

  open ( unit = output_unit, file = output_file_name, &
    status = 'replace', iostat = ios )

  if ( ios /= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'METIS_GRAPH_WRITE - Fatal error!'
    write ( *, '(a)' ) '  Could not open the output file.'
    stop
  end if

  write ( output_unit, '(a)' ) '%  MeTiS GraphFile "' &
    // trim ( output_file_name ) // '".'
  write ( output_unit, '(a)' ) '%'
  write ( output_unit, '(a)' ) '%  Each tetrahedron has four neighbors.'
  write ( output_unit, '(a)' ) '%  For each element, the elements that '
  write ( output_unit, '(a)' ) '%  share faces with it are listed.'

  i_positive = 0
  do j = 1, n
    do i = 1, m
      if ( 0 < neighbor(i,j) ) then
        i_positive = i_positive + 1
      end if
    end do
  end do

  n_elements = n
  n_faces = i_positive/2

  write ( output_unit, '(a)' ) '%'
  write ( output_unit, '(a)' ) '%  Output the number of elements and faces.'
  write ( output_unit, '(a)' ) '%'

  write ( string, '(a1,i8,a4)' ) '(', 2, 'i10)'
  call s_blank_delete ( string )
  write ( output_unit, string ) n_elements, n_faces

  write ( output_unit, '(a)' ) '%'
  write ( output_unit, '(a)' ) '%  For each element, list neighbors'
  write ( output_unit, '(a)' ) '%  that share faces with it.'
  write ( output_unit, '(a)' ) '%'

  allocate ( neighbors_of_element(1:m) )
  
  do j = 1, n

    i_positive = 0

    do i = 1, m
      if ( 0 < neighbor(i,j) ) then
        i_positive = i_positive + 1
        neighbors_of_element(i_positive) = neighbor(i,j)
      end if
    end do

    write ( string, '(a1,i8,a4)' ) '(', i_positive, 'i10)'
    call s_blank_delete ( string )

    write ( output_unit, string ) neighbors_of_element(1:i_positive)

  end do

  deallocate ( neighbors_of_element )

  write ( output_unit, '(a)' ) '%'
  write ( output_unit, '(a)' ) '%  End of file.'
  
  close ( unit = output_unit )

  return
end
subroutine s_blank_delete ( s )

!*****************************************************************************80
!
!! S_BLANK_DELETE removes blanks from a string, left justifying the remainder.
!
!  Discussion:
!
!    All TAB characters are also removed.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    26 July 1998
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input/output, character ( len = * ) S, the string to be transformed.
!
  implicit none

  character c
  integer ( kind = 4 ) get
  integer ( kind = 4 ) put
  integer ( kind = 4 ) nchar
  character ( len = * ) s
  character, parameter :: TAB = char ( 9 )

  put = 0
  nchar = len_trim ( s )

  do get = 1, nchar

    c = s(get:get)

    if ( c /= ' ' .and. c /= TAB ) then
      put = put + 1
      s(put:put) = c
    end if

  end do

  s(put+1:nchar) = ' '

  return
end
function s_index_last_c ( s, c )

!*****************************************************************************80
!
!! S_INDEX_LAST_C finds the LAST occurrence of a given character.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    06 December 2003
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) S, the string to be searched.
!
!    Input, character C, the character to search for.
!
!    Output, integer ( kind = 4 ) S_INDEX_LAST_C, the index in S where C occurs
!    last, or -1 if it does not occur.
!
  implicit none

  character c
  integer ( kind = 4 ) i
